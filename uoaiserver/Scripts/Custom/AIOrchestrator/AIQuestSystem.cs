        /// <summary>Complete a quest and give rewards.</summary>
        public static bool CompleteQuest(Mobile player, BaseCreature npc)
        {
            var playerSer = player.Serial.Value.ToString();
            var npcSer = npc.Serial.Value.ToString();

            AIQuest quest = null;
            lock (ActiveQuests)
            {
                quest = ActiveQuests.Values.FirstOrDefault(q =>
                    q.PlayerSerial == playerSer &&
                    q.GiverSerial == npcSer &&
                    q.IsCompleted && !q.IsExpired);
            }

            if (quest == null)
            {
                player.SendMessage("You have no completed quests for " + (npc.Name ?? "this NPC") + ".");
                return false;
            }

            // Give reward
            if (quest.RewardGold > 0)
            {
                var gold = new Gold(quest.RewardGold);
                if (player.Backpack != null && player.Backpack.TryDropItem(player, gold, false))
                {
                    player.SendMessage(0x44, $"[Quest] Reward received: {quest.RewardGold} gold!");
                }
                else
                {
                    // Drop at feet
                    gold.MoveToWorld(player.Location, player.Map);
                    player.SendMessage(0x44, $"[Quest] Reward dropped at your feet: {quest.RewardGold} gold.");
                }
            }

            // Track completion
            lock (CompletedQuestIds)
            {
                CompletedQuestIds.Add(quest.QuestId);
            }

            // Remove from active
            lock (ActiveQuests)
            {
                ActiveQuests.Remove(quest.QuestId);
            }

            // Record in NPC memory
            var component = AIComponentRegistry.GetComponent(npc);
            component?.Memory?.RecordHelp(playerSer, player.Name);

            // Apply faction reputation for quest completion
            FactionReputationSystem.OnQuestCompleted(player, npc);

            Console.WriteLine($"[AI QUEST] {player.Name} completed quest: {quest.QuestTitle}");
            return true;
        }

        /// <summary>Get quest status string for an NPC.</summary>
        public static string GetQuestContext(Mobile player, BaseCreature npc)
        {
            var playerSer = player.Serial.Value.ToString();
            var npcSer = npc.Serial.Value.ToString();

            lock (ActiveQuests)
            {
                var quest = ActiveQuests.Values.FirstOrDefault(q =>
                    q.PlayerSerial == playerSer &&
                    q.GiverSerial == npcSer &&
                    !q.IsExpired);

                if (quest == null)
                    return "";

                if (quest.IsCompleted)
                    return $"\n\nIMPORTANT: {player.Name} completed the quest '{quest.QuestTitle}'. Thank them and offer the reward.";

                return $"\n\nCurrent quest for {player.Name}: {quest.QuestDescription} (Progress: {quest.ObjectiveProgress}/{quest.ObjectiveCount})";
            }
        }

        private static void CleanupExpired()
        {
            lock (ActiveQuests)
            {
                var expired = ActiveQuests.Values.Where(q => q.IsExpired).ToList();
                foreach (var q in expired)
                {
                    ActiveQuests.Remove(q.QuestId);
                    Console.WriteLine($"[AI QUEST] Expired: {q.QuestTitle} for player {q.PlayerSerial}");
                }
            }
        }
    }

    /// <summary>
    /// Hooks into creature kill events to progress kill-count quests.
    /// </summary>
    public static class QuestCombatHook
    {
        public static void Initialize()
        {
            EventSink.CreatureDeath += OnCreatureDeath;
        }

        private static void OnCreatureDeath(CreatureDeathEventArgs e)
        {
            if (e.Killer?.Player != true) return;
            if (!(e.Creature is BaseCreature bc)) return;

            AIQuestManager.ReportKill(e.Killer, bc);
        }
    }

    /// <summary>
    /// Hooks for explore quests - when player enters a new region.
    /// </summary>
    public static class QuestExploreHook
    {
        public static void Initialize()
        {
            EventSink.Movement += OnMovement;
        }

        private static void OnMovement(MovementEventArgs e)
        {
            if (e.Mobile?.Player != true) return;

            var region = Region.Find(e.Mobile.Location, e.Mobile.Map);
            var oldRegion = Region.Find(new Point3D(e.Mobile.X - GetDirectionOffset(e.Direction).X,
                e.Mobile.Y - GetDirectionOffset(e.Direction).Y, e.Mobile.Z), e.Mobile.Map);

            if (region != oldRegion && region != null)
            {
                AIQuestManager.ReportExplore(e.Mobile, region);
            }
        }

        private static Point3D GetDirectionOffset(Direction dir)
        {
            int x = 0, y = 0;
            Server.Movement.Movement.Offset(dir, ref x, ref y);
            return new Point3D(x, y, 0);
        }
    }

    /// <summary>
    /// Extension helper for string truncation.
    /// </summary>
    public static class StringExtensions
    {
        public static string Truncate(this string value, int maxLength)
        {
            if (string.IsNullOrEmpty(value)) return value;
            return value.Length <= maxLength ? value : value.Substring(0, maxLength);
        }
    }
