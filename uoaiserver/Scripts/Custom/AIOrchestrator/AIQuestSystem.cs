            }
        }

        /// <summary>Progress a kill-count quest. Called when player kills a matching creature.
        /// Also handles CombatBoss and Bounty quests.</summary>
        public static void ReportKill(Mobile player, BaseCreature killed)
        {
            var playerSer = player.Serial.Value.ToString();
            var killName = killed.Name?.ToLowerInvariant() ?? killed.GetType().Name.ToLowerInvariant();

            lock (ActiveQuests)
            {
                // Check KillCount quests
                foreach (var quest in ActiveQuests.Values.Where(q =>
                    q.PlayerSerial == playerSer &&
                    !q.IsCompleted && !q.IsExpired &&
                    q.ObjectiveType == QuestObjectiveType.KillCount &&
                    q.ObjectiveTarget.ToLowerInvariant() == killName))
                {
                    quest.ObjectiveProgress++;
                    player.SendMessage(0x482, $"[Quest] {quest.QuestTitle}: {quest.ObjectiveProgress}/{quest.ObjectiveCount} {quest.ObjectiveTarget} killed.");

                    if (quest.ObjectiveProgress >= quest.ObjectiveCount)
                    {
                        quest.IsCompleted = true;
                        player.SendMessage(0x44, "[Quest] Objective complete! Return to " + quest.GiverNpcName + " for your reward.");
                    }
                }

                // Check CombatBoss quests (boss name matches creature name or type)
                foreach (var quest in ActiveQuests.Values.Where(q2 =>
                    q2.PlayerSerial == playerSer &&
                    !q2.IsCompleted && !q2.IsExpired &&
                    q2.ObjectiveType == QuestObjectiveType.CombatBoss))
                {
                    var questTarget = quest.ObjectiveTarget.ToLowerInvariant();
                    if (killName.Contains(questTarget) || questTarget.Contains(killName))
                    {
                        quest.ObjectiveProgress++;
                        player.SendMessage(0x482, $"[Quest] {quest.QuestTitle}: {quest.ObjectiveTarget} slain!");

                        quest.IsCompleted = true;
                        player.SendMessage(0x44, "[Quest] Boss slain! Return to " + quest.GiverNpcName + " for your reward.");
                    }
                }

                // Check Bounty quests
                foreach (var quest in ActiveQuests.Values.Where(q3 =>
                    q3.PlayerSerial == playerSer &&
                    !q3.IsCompleted && !q3.IsExpired &&
                    q3.ObjectiveType == QuestObjectiveType.Bounty))
                {
                    var questTarget = quest.ObjectiveTarget.ToLowerInvariant();
                    if (killName.Contains(questTarget) || questTarget.Contains(killName))
                    {
                        quest.ObjectiveProgress++;
                        player.SendMessage(0x482, $"[Quest] {quest.QuestTitle}: Bounty collected on {quest.ObjectiveTarget}!");

                        quest.IsCompleted = true;
                        player.SendMessage(0x44, "[Quest] Bounty complete! Return to " + quest.GiverNpcName + " for your reward.");
                    }
                }
            }
        }

        /// <summary>Progress a delivery quest. Called when player gives item to target NPC.</summary>
        public static void ReportDelivery(Mobile player, BaseCreature recipient, Item item)
        {
            if (item == null) return;
            var playerSer = player.Serial.Value.ToString();
            var recipientName = recipient.Name?.ToLowerInvariant() ?? "";

            lock (ActiveQuests)
            {
                // DeliverItem quests
                foreach (var quest in ActiveQuests.Values.Where(q =>
                    q.PlayerSerial == playerSer &&
                    !q.IsCompleted && !q.IsExpired &&
                    q.ObjectiveType == QuestObjectiveType.DeliverItem &&
                    q.SecondaryTarget.ToLowerInvariant().Contains(recipientName)))
                {
                    quest.ObjectiveProgress++;
                    player.SendMessage(0x482, $"[Quest] {quest.QuestTitle}: Delivered {item.Name} to {recipient.Name} ({quest.ObjectiveProgress}/{quest.ObjectiveCount}).");
                    item.Delete();

                    if (quest.ObjectiveProgress >= quest.ObjectiveCount)
                    {
                        quest.IsCompleted = true;
                        player.SendMessage(0x44, "[Quest] Objective complete! Return to " + quest.GiverNpcName + " for your reward.");
                    }
                }

                // MerchantRun quests
                foreach (var quest in ActiveQuests.Values.Where(q =>
                    q.PlayerSerial == playerSer &&
                    !q.IsCompleted && !q.IsExpired &&
                    q.ObjectiveType == QuestObjectiveType.MerchantRun &&
                    q.SecondaryTarget.ToLowerInvariant().Contains(recipientName)))
                {
                    var itemName = item.Name?.ToLowerInvariant() ?? item.GetType().Name.ToLowerInvariant();
                    if (quest.ObjectiveTarget.ToLowerInvariant().Contains(itemName))
                    {
                        quest.ObjectiveProgress++;
                        player.SendMessage(0x482, $"[Quest] {quest.QuestTitle}: Delivered {item.Name} to {recipient.Name} ({quest.ObjectiveProgress}/{quest.ObjectiveCount}).");
                        item.Delete();

                        if (quest.ObjectiveProgress >= quest.ObjectiveCount)
                        {
                            quest.IsCompleted = true;
                            player.SendMessage(0x44, "[Quest] Merchant run complete! Return to " + quest.GiverNpcName + " for your reward.");
                        }
                    }
                }
            }
        }

