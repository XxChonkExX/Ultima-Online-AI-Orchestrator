                case QuestObjectiveType.CollectItem:
                    return $"I need {quest.ObjectiveCount} {quest.ObjectiveTarget}. Bring them to me for a reward.";
                case QuestObjectiveType.DeliverItem:
                    return $"Take these {quest.ObjectiveCount} {quest.ObjectiveTarget} to {quest.SecondaryTarget}.";
                case QuestObjectiveType.Escort:
                    return $"Escort my friend to {quest.SecondaryTarget}. Keep them safe.";
                case QuestObjectiveType.Explore:
                    return $"Go to {quest.ObjectiveTarget} and tell me what you find.";
                case QuestObjectiveType.CombatBoss:
                    return $"The creature {quest.ObjectiveTarget} terrorizes our lands. Slay it and return for your reward of {quest.RewardGold} gold!";
                case QuestObjectiveType.MerchantRun:
                    return $"Purchase {quest.ObjectiveCount} {quest.ObjectiveTarget} and deliver them to {quest.SecondaryTarget}. I'll pay {quest.RewardGold} gold.";
                case QuestObjectiveType.CraftItem:
                    return $"Craft {quest.ObjectiveCount} {quest.ObjectiveTarget} and bring them to me. {quest.RewardGold} gold is your reward.";
                case QuestObjectiveType.NpcRelation:
                    return $"Travel to {quest.ObjectiveTarget} and speak with them {quest.ObjectiveCount} times to strengthen our ties.";
                case QuestObjectiveType.GuardDuty:
                    return $"Defend {quest.ObjectiveTarget} through {quest.ObjectiveCount} waves of attackers. I'll pay {quest.RewardGold} gold.";
                case QuestObjectiveType.Rescue:
                    return $"Rescue {quest.ObjectiveTarget} from {quest.SecondaryTarget}. Bring them back safely for {quest.RewardGold} gold.";
                case QuestObjectiveType.Scout:
                    return $"Scout {quest.ObjectiveTarget} and return with your report. {quest.RewardGold} gold for your trouble.";
