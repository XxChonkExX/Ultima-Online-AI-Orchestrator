        /// <summary>Is the player eligible for a new quest from this NPC?</summary>
        public static bool CanGiveQuest(Mobile player, BaseCreature npc)
        {
            if (player == null || npc == null) return false;

            var playerSer = player.Serial.Value.ToString();
            // Check if player already has an active quest from this NPC
            return !ActiveQuests.Values.Any(q =>
                q.PlayerSerial == playerSer &&
                q.GiverSerial == npc.Serial.Value.ToString() &&
                !q.IsCompleted && !q.IsExpired);
        }

        /// <summary>Generate a new quest using LLM.</summary>
        public static async Task<AIQuest> GenerateQuest(Mobile player, BaseCreature npc)
        {
            var playerSer = player.Serial.Value.ToString();
            var npcSer = npc.Serial.Value.ToString();

            // Pick a random objective type (expanded weights)
            var weights = new[] { 15, 10, 8, 5, 4, 8, 8, 8, 7, 7, 5, 8, 7 };
            // KillCount, Collect, Deliver, Escort, Explore, CombatBoss, MerchantRun,
            // CraftItem, NpcRelation, GuardDuty, Rescue, Scout, GatherResource, Bounty
            var total = weights.Sum();
            var roll = Utility.Random(total);
            var cumulative = 0;
            var objectiveType = QuestObjectiveType.KillCount;
            for (int i = 0; i < weights.Length; i++)
            {
                cumulative += weights[i];
                if (roll < cumulative)
                {
                    objectiveType = (QuestObjectiveType)i;
                    break;
                }
            }

            string target = "";
            string secondary = "";
            int count = 1;
            int goldReward = 100;
            Point3D dest = Point3D.Zero;
            Map destMap = Map.Felucca;

            switch (objectiveType)
            {
                case QuestObjectiveType.KillCount:
                    target = CreatureNames[Utility.Random(CreatureNames.Length)];
                    count = Utility.RandomMinMax(3, 12);
                    goldReward = count * Utility.RandomMinMax(25, 60);
                    break;

                case QuestObjectiveType.CollectItem:
                    target = ItemNames[Utility.Random(ItemNames.Length)];
                    count = Utility.RandomMinMax(3, 15);
                    goldReward = count * Utility.RandomMinMax(15, 40);
                    break;

                case QuestObjectiveType.DeliverItem:
                    target = ItemNames[Utility.Random(ItemNames.Length)];
                    secondary = DeliveryNpcNames[Utility.Random(DeliveryNpcNames.Length)];
                    count = Utility.RandomMinMax(1, 5);
                    goldReward = count * Utility.RandomMinMax(30, 80);
                    break;

                case QuestObjectiveType.Escort:
                    target = "a traveler";
                    secondary = LocationNames[Utility.Random(LocationNames.Length)];
                    count = 1;
                    goldReward = Utility.RandomMinMax(200, 500);
                    dest = new Point3D(npc.X + Utility.RandomMinMax(-20, 20),
                                       npc.Y + Utility.RandomMinMax(-20, 20), npc.Z);
                    destMap = npc.Map;
                    break;

                case QuestObjectiveType.Explore:
                    target = LocationNames[Utility.Random(LocationNames.Length)];
                    secondary = "";
                    count = 1;
                    goldReward = Utility.RandomMinMax(150, 400);
                    dest = new Point3D(npc.X + Utility.RandomMinMax(-30, 30),
                                       npc.Y + Utility.RandomMinMax(-30, 30), npc.Z);
                    destMap = npc.Map;
                    break;

                case QuestObjectiveType.CombatBoss:
                    target = BossNames[Utility.Random(BossNames.Length)];
                    count = 1;
                    goldReward = Utility.RandomMinMax(500, 2000);
                    break;

                case QuestObjectiveType.MerchantRun:
                    target = TradeItems[Utility.Random(TradeItems.Length)];
                    secondary = DeliveryNpcNames[Utility.Random(DeliveryNpcNames.Length)];
                    count = Utility.RandomMinMax(1, 5);
                    goldReward = count * Utility.RandomMinMax(50, 120);
                    break;

                case QuestObjectiveType.CraftItem:
                    target = CraftableItems[Utility.Random(CraftableItems.Length)];
                    count = Utility.RandomMinMax(1, 5);
                    goldReward = count * Utility.RandomMinMax(60, 150);
                    break;

                case QuestObjectiveType.NpcRelation:
                    target = NpcRelationTargets[Utility.Random(NpcRelationTargets.Length)];
                    count = Utility.RandomMinMax(2, 5); // Number of conversations
                    goldReward = count * Utility.RandomMinMax(20, 40);
                    break;

                case QuestObjectiveType.GuardDuty:
                    target = GuardLocations[Utility.Random(GuardLocations.Length)];
                    count = Utility.RandomMinMax(3, 8); // Number of waves
                    goldReward = Utility.RandomMinMax(200, 600);
                    dest = new Point3D(npc.X + Utility.RandomMinMax(-10, 10),
                                       npc.Y + Utility.RandomMinMax(-10, 10), npc.Z);
                    destMap = npc.Map;
                    break;

                case QuestObjectiveType.Rescue:
                    target = "a captured citizen";
                    secondary = LocationNames[Utility.Random(LocationNames.Length)];
                    count = 1;
                    goldReward = Utility.RandomMinMax(300, 800);
                    break;

                case QuestObjectiveType.Scout:
                    target = LocationNames[Utility.Random(LocationNames.Length)];
                    count = 1;
                    goldReward = Utility.RandomMinMax(100, 300);
                    break;

                case QuestObjectiveType.GatherResource:
                    target = ResourceTypes[Utility.Random(ResourceTypes.Length)];
                    count = Utility.RandomMinMax(10, 50);
                    goldReward = count * Utility.RandomMinMax(3, 8);
                    break;

                case QuestObjectiveType.Bounty:
                    target = BossNames[Utility.Random(BossNames.Length)];
                    count = 1;
                    goldReward = Utility.RandomMinMax(800, 3000);
                    break;
            }

            var quest = new AIQuest
            {
                GiverNpcName = npc.Name ?? "the NPC",
                QuestTitle = $"Help the {npc.Name ?? "Village"}",
                ObjectiveType = objectiveType,
                ObjectiveTarget = target,
                ObjectiveCount = count,
                RewardGold = goldReward,
                RewardDescription = $"{goldReward} gold coins",
                PlayerSerial = playerSer,
                GiverSerial = npcSer,
                SecondaryTarget = secondary,
                Destination = dest,
                DestinationMap = destMap
            };

            // Ask LLM to generate flavor text
            var identity = AIComponentRegistry.GetComponent(npc)?.Memory?.Identity;
            var identityPrompt = identity?.GetPersonalityPrompt() ?? "You are an NPC in Ultima Online.";

            var questPrompt = BuildQuestPrompt(identityPrompt, player.Name, quest);

            try
            {
                var reply = await LLMClient.ChatAsync("", questPrompt, AIConfig.ModelDialogue);
                if (!string.IsNullOrEmpty(reply))
                {
                    var parts = reply.Split('|');
                    if (parts.Length >= 2)
                    {
                        quest.QuestTitle = parts[0].Trim().Truncate(40);
                        quest.QuestDescription = parts[1].Trim().Truncate(160);
                    }
                    else
                    {
                        quest.QuestDescription = reply.Truncate(160);
                    }
                }
                else
                {
                    quest.QuestDescription = GetFallbackDescription(quest);
                }
            }
            catch
            {
                quest.QuestDescription = GetFallbackDescription(quest);
            }

            lock (ActiveQuests)
            {
                ActiveQuests[quest.QuestId] = quest;
            }

            // For Escort: spawn the escort NPC after quest is registered
            if (quest.ObjectiveType == QuestObjectiveType.Escort)
            {
                Timer.DelayCall(TimeSpan.FromSeconds(1), () => SpawnEscortNPC(quest, npc));
            }

            Console.WriteLine($"[AI QUEST] {npc.Name} -> {player.Name}: [{quest.ObjectiveType}] \"{quest.QuestDescription}\" (reward: {goldReward}g)");
            return quest;
        }

        private static void SpawnEscortNPC(AIQuest quest, BaseCreature giver)
        {
            try
            {
                // Create a simple escort NPC (using a generic BaseCreature)
                var escort = new BaseCreature(AIType.AI_Melee, FightMode.Closest, 10, 1, 0.2, 0.4)
                {
                    Name = "a nervous traveler",
                    Body = 0x190, // Human male
                    Hue = Utility.RandomSkinHue(),
                    ControlSlots = 0,
                    Tamable = false,
                    ControlMaster = null
                };

                // Place near giver
                var loc = new Point3D(giver.X + Utility.RandomMinMax(-2, 2), giver.Y + Utility.RandomMinMax(-2, 2), giver.Z);
                escort.MoveToWorld(loc, giver.Map);

                // Make them follow the player
                var player = World.FindMobile((Serial)(int)uint.Parse(quest.PlayerSerial));
                if (player != null)
                {
                    escort.ControlMaster = player;
                    escort.ControlTarget = player;
                    escort.ControlOrder = OrderType.Follow;
                }

                // Store reference on quest
                quest.EscortNpcSerial = escort.Serial;

                Console.WriteLine($"[AI QUEST] Spawned escort NPC for {quest.QuestId}");
            }
            catch (Exception ex)
            {
                Console.WriteLine($"[AI QUEST] Escort spawn error: {ex.Message}");
            }
        }

        private static string BuildQuestPrompt(string identityPrompt, string playerName, AIQuest quest)
        {
            var basePrompt = $"{identityPrompt}\n\nGenerate a quest for {playerName}. ";

            string details;
            switch (quest.ObjectiveType)
            {
                case QuestObjectiveType.KillCount:
                    details = $"Ask them to kill {quest.ObjectiveCount} {quest.ObjectiveTarget}(s). Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.CollectItem:
                    details = $"Ask them to bring you {quest.ObjectiveCount} {quest.ObjectiveTarget}. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.DeliverItem:
                    details = $"Ask them to deliver {quest.ObjectiveCount} {quest.ObjectiveTarget} to {quest.SecondaryTarget}. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.Escort:
                    details = $"Ask them to escort someone to {quest.SecondaryTarget}. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.Explore:
                    details = $"Ask them to explore {quest.ObjectiveTarget} and report back. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.CombatBoss:
                    details = $"Ask them to slay the fearsome {quest.ObjectiveTarget}. Reward: {quest.RewardGold} gold. This is a dangerous bounty!";
                    break;
                case QuestObjectiveType.MerchantRun:
                    details = $"Ask them to purchase {quest.ObjectiveCount} {quest.ObjectiveTarget}(s) and deliver them to {quest.SecondaryTarget}. They'll need gold upfront. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.CraftItem:
                    details = $"Ask them to craft {quest.ObjectiveCount} {quest.ObjectiveTarget}(s) and bring them to you. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.NpcRelation:
                    details = $"Ask them to travel to {quest.ObjectiveTarget} and exchange pleasantries {quest.ObjectiveCount} times to build goodwill. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.GuardDuty:
                    details = $"Ask them to defend {quest.ObjectiveTarget} for {quest.ObjectiveCount} waves of attackers. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.Rescue:
                    details = $"Ask them to rescue {quest.ObjectiveTarget} from {quest.SecondaryTarget}. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.Scout:
                    details = $"Ask them to scout {quest.ObjectiveTarget} and report what they find. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.GatherResource:
                    details = $"Ask them to gather {quest.ObjectiveCount} units of {quest.ObjectiveTarget}. Reward: {quest.RewardGold} gold.";
                    break;
                case QuestObjectiveType.Bounty:
                    details = $"Post a bounty on {quest.ObjectiveTarget}. {quest.RewardGold} gold for the head of this villain!";
                    break;
                default:
                    details = "";
                    break;
            }

            return basePrompt + details +
                   $"\n\nOutput format: TITLE|DESCRIPTION\n" +
                   $"Title should be short. Description should be 1 sentence (max 160 chars) in your character's voice.";
        }

