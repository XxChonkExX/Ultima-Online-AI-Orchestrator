    #region Data Types

    public enum QuestObjectiveType
    {
        KillCount,      // Kill X of creature type Y
        CollectItem,    // Bring X of item Y to quest giver
        DeliverItem,    // Take item Y to NPC Z
        Escort,         // Escort NPC Z to location
        Explore,        // Visit location Y
        CombatBoss,     // Kill a named boss/strong creature
        MerchantRun,    // Buy X items and bring them (trade route)
        CraftItem,      // Craft X of specific item type
        NpcRelation,    // Build reputation with another NPC (talk to them multiple times)
        GuardDuty,      // Defend a location from attackers
        Rescue,         // Rescue a captured NPC from a dungeon
        Scout,          // Scout a dangerous area and report back
        GatherResource, // Mine/chop/fish X amount of a resource
        Bounty          // Kill a specific named creature (bounty)
    }

    public class AIQuest
    {
        public string QuestId { get; set; } = Guid.NewGuid().ToString("N").Substring(0, 8);
        public string GiverNpcName { get; set; }
        public string QuestTitle { get; set; }
        public string QuestDescription { get; set; }
        public QuestObjectiveType ObjectiveType { get; set; }
        public string ObjectiveTarget { get; set; }     // creature type / item name / NPC name / location name
        public int ObjectiveCount { get; set; } = 1;
        public int ObjectiveProgress { get; set; } = 0;
        public string RewardDescription { get; set; }
        public int RewardGold { get; set; } = 100;
        public DateTime IssuedAt { get; set; } = DateTime.UtcNow;
        public DateTime ExpiresAt { get; set; } = DateTime.UtcNow.AddHours(4);
        public bool IsCompleted { get; set; }
        public bool IsExpired => DateTime.UtcNow > ExpiresAt;
        public string PlayerSerial { get; set; }
        public string GiverSerial { get; set; }

        // For DeliverItem/Escort/Explore
        public string SecondaryTarget { get; set; }    // destination NPC name / location name
        public Point3D Destination { get; set; }        // for Escort/Explore
        public Map DestinationMap { get; set; }
        public Item QuestItem { get; set; }             // for DeliverItem - the item to deliver
        public Serial EscortNpcSerial { get; set; }     // serial of escort NPC
    }

    #endregion

